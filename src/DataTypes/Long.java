package DataTypes;

// Syntax: long variableName;
public class Long {
    public static void main(String[] args) {
        // Declaration of long variables
        long variableOne = 1L;
        long variableTwo = 2L;

        // Display Output
        System.out.println("Description of variableOne: " + variableOne);
        System.out.println("Description of variableTwo: " + variableTwo);
    }
}