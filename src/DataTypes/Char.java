package DataTypes;

// Syntax: char variableName;
public class Char {
    public static void main(String[] args) {
        // Declaration of char variables
        char variableOne = 'X';
        char variableTwo = '#';

        // Display Output
        System.out.println("Character One: " + variableOne);
        System.out.println("Character Two: " + variableTwo);
    }
}