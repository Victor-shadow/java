package DataTypes;

public class DataTypes {
    public static void main(String[] args) {
        // Declaration of primitive data types
        char variableOne = 'X';
        int variableTwo = 2;
        byte variableThree = 3;
        short variableFour = 4;
        double variableFive = 5.0;
        float variableSix = 0.0f;
        long variableSeven = 0L;

        // Display Output
        System.out.println("char: " + variableOne);
        System.out.println("int: " + variableTwo);
        System.out.println("byte: " + variableThree);
        System.out.println("short: " + variableFour);
        System.out.println("double: " + variableFive);
        System.out.println("float: " + variableSix);
        System.out.println("long: " + variableSeven);
    }
}