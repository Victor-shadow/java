package Arrays;
import java.util.Arrays;
//The method is used to compute the hash code of an array(Arrays.hashCode)
public class HashCodeMethod {
    public static void main(String[]args){
        //Array Initialization
        int[]initArray ={10, 20, 15, 22, 35};
        //To get HashCode of the Arrays
        System.out.println("Integer Array: " + Arrays.hashCode(initArray));


    }
}
