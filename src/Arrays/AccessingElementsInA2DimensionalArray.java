package Arrays;

public class AccessingElementsInA2DimensionalArray {
    public static void main(String[] args){
        int[][] arr = { { 1, 2 },
                { 3, 4 } };

        // Accessing Element at index
        // row=1 and column=1
        System.out.println("a[1][1] : " + arr[1][1]);

    }
}
